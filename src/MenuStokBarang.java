    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String Kode = jTextField1.getText();
        String Nama = jTextField2.getText();
        String Kategori = jTextField7.getText();
        String Harga = jTextField4.getText();
        String StokKurang = jTextField5.getText();
        String Stok = jTextField3.getText();
        int stok_int = Stok.equals("") ? 0 : Integer.parseInt(Stok);
        int stok_isi = StokKurang.equals("") ? 0 : Integer.parseInt(StokKurang);
        if(Kode.equals("")||Nama.equals("")||Kategori.equals("")||Harga.equals("")||StokKurang.equals("")){
            JOptionPane.showMessageDialog(null, "Pilih Data yang ingin dikurangi stoknya!");
        }else if(stok_isi <= 0){
            JOptionPane.showMessageDialog(null, "Nilai Stok Kurang, minimal isi 1");
        }else if(stok_isi > stok_int){
            JOptionPane.showMessageDialog(null, "Nilai Stok Kurang tidak boleh lebih besar dari Stok saat ini");
        }else{
            try{
               String Vsql ="UPDATE tb_stok SET stok = stok-'"+StokKurang+"' WHERE kode_barang = '"+Kode+"'";
               java.sql.Connection VCon = (Connection)KoneksiDB.configDB();
               java.sql.PreparedStatement S = VCon.prepareStatement(Vsql);
               S.execute();
               JOptionPane.showMessageDialog(null, "Mengurangi Stok Berhasil!");
               tampil_tabel();
               bersih();
            }catch (Exception e){
               JOptionPane.showMessageDialog(null, "Mengurangi Stok Gagal!");
               JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

private void tampil_tabel(){
    //membuat objek model pada tabel
    DefaultTableModel tb = new DefaultTableModel();
    tb.addColumn("No");
    tb.addColumn("Kode");
    tb.addColumn("Nama");
    tb.addColumn("Kategori");
    tb.addColumn("Harga");
    tb.addColumn("Stok");

    try{
        int counter = 1;
        //1. Query
        String query = "SELECT * FROM tb_stok";
        //2. Fungsi Koneksi
        java.sql.Connection VCon = (Connection)KoneksiDB.configDB();
        //3. Kirim Parameter Fungsi Java ke SQL
        java.sql.Statement S = VCon.createStatement();
        //4. Eksekusi Query
        java.sql.ResultSet R = S.executeQuery(query);
        //5. Looping
        while(R.next()){
            tb.addRow(new Object[]{
                counter++,R.getString(1),R.getString(2),R.getString(3), R.getInt(4), R.getInt(5)
            });
        }
        jTable1.setModel(tb);
    }catch(Exception e){
    }
}

