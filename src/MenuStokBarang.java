    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String Kode = jTextField1.getText();
        String Nama = jTextField2.getText();
        String Kategori = jTextField7.getText();
        String Harga = jTextField4.getText();
        String StokTambah = jTextField5.getText();
        String Stok = jTextField3.getText();
        int stok_int = Stok.equals("") ? 0 : Integer.parseInt(Stok);
        int stok_isi = StokTambah.equals("") ? 0 : Integer.parseInt(StokTambah);

        if(Kode.equals("")||Nama.equals("")||Kategori.equals("")||Harga.equals("")||StokTambah.equals("")){
            JOptionPane.showMessageDialog(null, "Pilih Data yang ingin ditambah stoknya!");
        }else if(stok_isi <= 0){
            JOptionPane.showMessageDialog(null, "Nilai Stok Tambah, minimal isi 1");
        }else{
            try{
               String Vsql ="UPDATE tb_stok SET stok = stok+'"+StokTambah+"' WHERE kode_barang = '"+Kode+"'";
               java.sql.Connection VCon = (Connection)KoneksiDB.configDB();
               java.sql.PreparedStatement S = VCon.prepareStatement(Vsql);
               S.execute();
               JOptionPane.showMessageDialog(null, "Menambah Stok Berhasil!");
               tampil_tabel();
               bersih();
            }catch (Exception e){
               JOptionPane.showMessageDialog(null, "Menambah Stok Gagal!");
               JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

